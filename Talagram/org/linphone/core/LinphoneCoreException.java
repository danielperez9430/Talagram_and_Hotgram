package org.linphone.core;

public class LinphoneCoreException extends Exception {
    public LinphoneCoreException(String arg1) {
        super(arg1);
    }

    public LinphoneCoreException() {
        super();
    }

    public LinphoneCoreException(String arg1, Throwable arg2) {
        super(arg1, arg2);
    }

    public LinphoneCoreException(Throwable arg1) {
        super(arg1);
    }
}

