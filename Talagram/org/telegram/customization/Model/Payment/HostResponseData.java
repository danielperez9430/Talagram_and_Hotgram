package org.telegram.customization.Model.Payment;

public class HostResponseData {
    String hresp;
    String hsign;

    public HostResponseData() {
        super();
    }

    public String getHresp() {
        return this.hresp;
    }

    public String getHsign() {
        return this.hsign;
    }

    public void setHresp(String arg1) {
        this.hresp = arg1;
    }

    public void setHsign(String arg1) {
        this.hsign = arg1;
    }
}

