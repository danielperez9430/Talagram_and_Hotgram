package org.telegram.customization.Model.voip;

public class MetaHelper {
    int code;
    String message;

    public MetaHelper() {
        super();
    }

    public int getCode() {
        return this.code;
    }

    public String getMessage() {
        return this.message;
    }

    public void setCode(int arg1) {
        this.code = arg1;
    }

    public void setMessage(String arg1) {
        this.message = arg1;
    }
}

