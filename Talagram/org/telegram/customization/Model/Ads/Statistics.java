package org.telegram.customization.Model.Ads;

public class Statistics {
    int clickCount;
    int joinCount;
    int score;
    int urlCount;
    int viewCount;

    public Statistics() {
        super();
    }

    public int getClickCount() {
        return this.clickCount;
    }

    public int getJoinCount() {
        return this.joinCount;
    }

    public int getScore() {
        return this.score;
    }

    public int getUrlCount() {
        return this.urlCount;
    }

    public int getViewCount() {
        return this.viewCount;
    }

    public void setClickCount(int arg1) {
        this.clickCount = arg1;
    }

    public void setJoinCount(int arg1) {
        this.joinCount = arg1;
    }

    public void setScore(int arg1) {
        this.score = arg1;
    }

    public void setUrlCount(int arg1) {
        this.urlCount = arg1;
    }

    public void setViewCount(int arg1) {
        this.viewCount = arg1;
    }
}

