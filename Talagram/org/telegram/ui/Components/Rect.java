package org.telegram.ui.Components;

public class Rect {
    public float height;
    public float width;
    public float x;
    public float y;

    public Rect(float arg1, float arg2, float arg3, float arg4) {
        super();
        this.x = arg1;
        this.y = arg2;
        this.width = arg3;
        this.height = arg4;
    }

    public Rect() {
        super();
    }
}

