package org.telegram.customization.Model;

public class Setting {
    String key;
    String value;

    public Setting() {
        super();
    }

    public String getKey() {
        return this.key;
    }

    public String getValue() {
        return this.value;
    }

    public void setKey(String arg1) {
        this.key = arg1;
    }

    public void setValue(String arg1) {
        this.value = arg1;
    }
}

