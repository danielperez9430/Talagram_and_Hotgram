package org.telegram.customization.Model;

public class UserState {
    long e;
    int gp;
    int gr;
    int mr;
    int ms;
    long s;
    int vc;

    public UserState() {
        super();
    }

    public long getE() {
        return this.e;
    }

    public int getGp() {
        return this.gp;
    }

    public int getGr() {
        return this.gr;
    }

    public int getMr() {
        return this.mr;
    }

    public int getMs() {
        return this.ms;
    }

    public long getS() {
        return this.s;
    }

    public int getVc() {
        return this.vc;
    }

    public void setE(long arg1) {
        this.e = arg1;
    }

    public void setGp(int arg1) {
        this.gp = arg1;
    }

    public void setGr(int arg1) {
        this.gr = arg1;
    }

    public void setMr(int arg1) {
        this.mr = arg1;
    }

    public void setMs(int arg1) {
        this.ms = arg1;
    }

    public void setS(long arg1) {
        this.s = arg1;
    }

    public void setVc(int arg1) {
        this.vc = arg1;
    }
}

