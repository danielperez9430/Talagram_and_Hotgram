package org.telegram.customization.Model.Payment;

public class Bank {
    int id;
    String name;

    public Bank() {
        super();
    }

    public int getId() {
        return this.id;
    }

    public String getName() {
        return this.name;
    }

    public void setId(int arg1) {
        this.id = arg1;
    }

    public void setName(String arg1) {
        this.name = arg1;
    }
}

