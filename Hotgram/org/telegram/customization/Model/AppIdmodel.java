package org.telegram.customization.Model;

public class AppIdmodel {
    String ah;
    int ai;
    String name;

    public AppIdmodel() {
        super();
    }

    public String getAh() {
        return this.ah;
    }

    public int getAi() {
        return this.ai;
    }

    public String getName() {
        return this.name;
    }

    public void setAh(String arg1) {
        this.ah = arg1;
    }

    public void setAi(int arg1) {
        this.ai = arg1;
    }

    public void setName(String arg1) {
        this.name = arg1;
    }
}

