package org.telegram.customization.dynamicadapter.data;

public class ExtraData {
    public int poolId;
    long tagId;

    public ExtraData() {
        super();
    }

    public int getPoolId() {
        return this.poolId;
    }

    public long getTagId() {
        return this.tagId;
    }

    public void setPoolId(int arg1) {
        this.poolId = arg1;
    }

    public void setTagId(long arg1) {
        this.tagId = arg1;
    }
}

