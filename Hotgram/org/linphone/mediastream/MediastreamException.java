package org.linphone.mediastream;

public class MediastreamException extends Exception {
    public MediastreamException() {
        super();
    }

    public MediastreamException(String arg1) {
        super(arg1);
    }

    public MediastreamException(String arg1, Throwable arg2) {
        super(arg1, arg2);
    }

    public MediastreamException(Throwable arg1) {
        super(arg1);
    }
}

